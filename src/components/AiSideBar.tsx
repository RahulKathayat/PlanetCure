const AiSideBar = () => {
    const [extended, setExtended] = useState<boolean>(false);
    const { onSet, prvePrompt, setrecentPrompt, newChat,setinput } = useContext(Context);
    const loadPrompt =  (prompt:string) => {
        setrecentPrompt(prompt);
        onSet(undefined);
      }
    return (
        <div className='sidebar border-r'>
            <div className='top'>
                <MenuIcon setExtended={setExtended} />
                <div className="new-chat">
                    <PlusIcon />
                    {extended ? <p>New Chat</p> : null}
                </div>
                {extended ?
                    <div className="recent">
                        <p className="recent-title">
                            Recent
                        </p>
                        {prvePrompt.map((item:string, index:number) => {
                            return (

                                <div onClick={async () => {
                                    setinput(item);
                                    await loadPrompt(item);
                                }} className="recent-entry" key={index}>
                                    <MsgIcon />
                                    <p>{item.slice(0, 18)}</p>
                                </div>
                            )
                        })}
                    </div>
                    : null
                }
            </div>
            <div className='bottom'>
                <div className="bottom-item recent-entry">
                    <HelpIcon />
                    {extended && <p>Help</p>}
                </div>
                <div className="bottom-item recent-entry">
                    <HistoryIcon />
                    {extended && <p>Activity</p>}
                </div>
                <div className="bottom-item recent-entry">
                    <SettingIcon />
                    {extended && <p>Settings</p>}
                </div>
            </div>
        </div>
    )
}

export default AiSideBar